#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = s;
	while (n--)
	{
		str[i] = c;
		i++;
	}
	return (s);
}
