int	ft_atoi(char *c)
{
	int	s;
	int	i;

	s = 1;
	i = 0;
	while (*c == 32 || (*c >= 9 && *c <= 13))
		c++;
	if (*c == '-' || *c == '+')
	{
		if (*c == '-')
			s = -s;
		c++;
	}
	while (*c >= 48 && *c <= 57)
		i = (i * 10) + (*c++ - 48);
	return (s * i);
}
