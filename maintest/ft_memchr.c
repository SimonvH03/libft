#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	chr;

	str = (unsigned char *)s;
	chr = (unsigned char) c;
	while (n-- && str++)
		if (*str == chr)
			return ((void *)str);
	return ((void *)0);
}
